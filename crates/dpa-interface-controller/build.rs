use std::path::PathBuf;

fn main() -> Result<(), Box<dyn std::error::Error>> {
    let out_dir = PathBuf::from(std::env::var_os("OUT_DIR").unwrap());
    // Write generated code to OUT_DIR rather than back into the source tree.
    // Consumers pull these in via:
    // `include!(concat!(env!("OUT_DIR"), "/X.rs"))` in `src/protos/mod.rs`
    let autogenerated_rust_output_directory = out_dir.clone();
    // The reflection path is part of the output directory (which might be debug or release),
    // so that different builds get a different binary file and concurrent builds don't collide
    let reflection = out_dir.join("nico.bin");

    tonic_prost_build::configure()
        .file_descriptor_set_path(reflection)
        .protoc_arg("--experimental_allow_proto3_optional")
        .out_dir(autogenerated_rust_output_directory)
        .compile_protos(&["proto/dpa_rpc.proto"], &["proto"])
        .unwrap();

    Ok(())
}
